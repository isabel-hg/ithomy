// Variable para rastrear la sección actual
let currentSection = 'iRECIPE';

// Función para mostrar/ocultar elementos
function showElement(elementId) {
    const element = document.getElementById(elementId);
    if (element) {
        element.style.display = 'block';
        element.style.opacity = '1';
    }
}

function hideElement(elementId) {
    const element = document.getElementById(elementId);
    if (element) {
        element.style.display = 'none';
    }
}

function hideWithOpacity(elementId) {
    const element = document.getElementById(elementId);
    if (element) {
        element.style.opacity = '0';
    }
}

// Función para cambiar el ancho del TV
function changeTvWidth(elementId, width) {
    const element = document.getElementById(elementId);
    if (element) {
        element.style.width = width;
    }
}

// Función para cambiar contenido de texto
function changeText(elementId, newText) {
    const element = document.getElementById(elementId);
    if (element) {
        element.innerHTML = newText;
    }
}

// Función para cambiar imagen
function changeImage(elementId, newSrc) {
    const element = document.getElementById(elementId);
    if (element) {
        element.src = newSrc;
    }
}

// Función para resetear a estado inicial
function resetToDefault() {
    // Mostrar todos los elementos
    showElement('text-container');
    showElement('text');
    
    // Restaurar contenido por defecto
    changeImage('tv', iRECIPE_IMAGE);
    changeText('text', iRECIPE_PART_1);
    changeImage('text-bg', DEFAULT_BACKGROUND);
}

// Función para cambiar a una parte específica del texto según la sección actual
function changeToTextPart(partNumber) {
    if (currentSection === 'iPLE' || currentSection === 'iBROCHURE') {
        return; // No hacer nada si estamos en iPLE o iBROCHURE (no tienen texto visible)
    }
    
    let textContent = '';
    
    switch (currentSection) {
        case 'iRECIPE':
            if (partNumber === 1) textContent = iRECIPE_PART_1;
            else if (partNumber === 2) textContent = iRECIPE_PART_2;
            else if (partNumber === 3) textContent = iRECIPE_PART_3;
            break;
        case 'iBUFFY':
            if (partNumber === 1) textContent = iBUFFY_PART_1;
            else if (partNumber === 2) textContent = iBUFFY_PART_2;
            else if (partNumber === 3) textContent = iBUFFY_PART_3;
            break;
        case 'iCHRONICLE':
            if (partNumber === 1) textContent = iCHRONICLE_PART_1;
            else if (partNumber === 2) textContent = iCHRONICLE_PART_2;
            else if (partNumber === 3) textContent = iCHRONICLE_PART_3;
            break;
        case 'iTHOMY':
            textContent = iTHOMY_PART_1; // iTHOMY solo tiene una parte
            break;
    }
    
    if (textContent) {
        changeText('text', textContent);
    }
}

// Event listeners para cada botón del menú
document.addEventListener('DOMContentLoaded', function() {
    // iRECIPE
    document.getElementById('iRECIPE').addEventListener('click', function() {
        currentSection = 'iRECIPE';
        showElement('text-container');
        showElement('text');
        changeImage('tv', iRECIPE_IMAGE);
        changeText('text', iRECIPE_PART_1);
        changeImage('text-bg', DEFAULT_BACKGROUND);
        changeTvWidth('tv', '40%');
    });

    // iBUFFY
    document.getElementById('iBUFFY').addEventListener('click', function() {
        currentSection = 'iBUFFY';
        showElement('text-container');
        showElement('text');
        changeImage('tv', iBUFFY_IMAGE);
        changeText('text', iBUFFY_PART_1);
        changeImage('text-bg', DEFAULT_BACKGROUND);
        changeTvWidth('tv', '40%');
    });

    // iPLE
    document.getElementById('iPLE').addEventListener('click', function() {
        currentSection = 'iPLE';
        hideWithOpacity('text-container');
        changeImage('tv', iPLE_IMAGE);
        changeTvWidth('tv', '50%');
    });

    // iBROCHURE
    document.getElementById('iBROCHURE').addEventListener('click', function() {
        currentSection = 'iBROCHURE';
        showElement('text-container');
        hideElement('text');
        changeImage('tv', iBROCHURE_IMAGE);
        changeImage('text-bg', iBROCHURE_BACKGROUND);
        changeTvWidth('tv', '40%');
    });

    // iCHRONICLE
    document.getElementById('iCHRONICLE').addEventListener('click', function() {
        currentSection = 'iCHRONICLE';
        showElement('text-container');
        showElement('text');
        changeImage('tv', iCHRONICLE_IMAGE);
        changeText('text', iCHRONICLE_PART_1);
        changeImage('text-bg', DEFAULT_BACKGROUND);
        changeTvWidth('tv', '40%');
    });

    // iTHOMY (botón info)
    document.getElementById('iTHOMY').addEventListener('click', function() {
        currentSection = 'iTHOMY';
        showElement('text-container');
        showElement('text');
        changeImage('tv', iTHOMY_IMAGE);
        changeText('text', iTHOMY_PART_1);
        changeImage('text-bg', DEFAULT_BACKGROUND);
        changeTvWidth('tv', '40%');
    });

    // Event listeners para los botones del SVG signals usando clases
    document.getElementById('signals').addEventListener('click', function(event) {
        const clickedElement = event.target;
        
        // Verificar si el elemento clickeado tiene una de las clases de botón
        if (clickedElement.classList.contains('svg-button-part-1')) {
            changeToTextPart(1);
        } else if (clickedElement.classList.contains('svg-button-part-2')) {
            changeToTextPart(2);
        } else if (clickedElement.classList.contains('svg-button-part-3')) {
            changeToTextPart(3);
        }
    });
});